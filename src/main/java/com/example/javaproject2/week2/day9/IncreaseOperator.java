package com.example.javaproject2.week2.day9;

public class IncreaseOperator {
    public static void main(String[] args) {
        int i = 10;
        // ++i가 앞에 오는 경우
        System.out.println(++i); //사채시장=선이자를 올린다. = 일단 올린다
        System.out.println(i);

        System.out.println(i++);
        System.out.println(i);
    }
}
