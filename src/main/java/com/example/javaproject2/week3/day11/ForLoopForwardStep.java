package com.example.javaproject2.week3.day11;

public class ForLoopForwardStep {
    public static void main(String[] args) {
        for(int i =0; i<10; i +=2){ //짝수
            System.out.println(i);
        }

        System.out.println("-------------------");
        for(int i =1; i<10; i +=2){//홀수
            System.out.println(i);
        }
    }
}
